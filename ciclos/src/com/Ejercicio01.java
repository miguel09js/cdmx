package com;
/*
 * Tabla de multiplicar del 12
 */
public class Ejercicio01 {

public static void main(String[] args) {
		
		System.out.println("TABLA DE MULTIPLICAR DEL 12");
		
		int tabla = 12;
		int i=1;
		
		while (i<=10) {
			System.out.println(tabla+" x "+ i + " = " + (tabla*i));
			i++;
		}
	}
}
